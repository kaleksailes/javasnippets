
public class GetterSetters {

    // String
    private String alphanumeric;

    public String getLine() {
        return alphanumeric;
    }

    public void setLine(String x) {
        this.alphanumeric = x;
    }

    // Integer
    private int numeric;

    public int getNum() {
        return numeric;
    }

    public void setNum(int x) {
        this.numeric = x;
    }

}